package JavaInterviewQuestions;
// Q1. Print number 1 to 100 without using number;
public class Q1_PrintNumberWithoutUsingNumber {
    public static void main(String[] args) {

        int one='A'/'A';
        System.out.println(one);
        String str="..........";
        int len=str.length()*10;
        System.out.println(len);
        for(int i=one;i<=len;i++)
        {
            System.out.print(i+",");
        }

    }
}
